namespace AT{
	//---------------------------------------------------------------------------
	template <typename unit> struct QUAT{
		//----
		unit i, j, k, w;
		//----
					QUAT()			{ Zero(); }
		void		Zero()			{ i=j=k=0; w=1; }
		void        Normalize()		{ unit n = 1.0f/sqrt(i*i+j*j+k*k+w*w); i*=n; j*=n; k*=n; w*=n; }
		void		FromAngle(unit angle, unit x, unit y, unit z){
				w = cos(angle/(unit)2);
				unit s = sin(angle/(unit)2);
				i = x*s;
				j = y*s;
				k = z*s;
		}
	};
	//---------------------------------------------------------------------------
}//namespace AT
